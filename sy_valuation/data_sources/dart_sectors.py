def _norm(code: str | None) -> str:
    if not code:
        return ""
    return str(code).strip()


def map_induty(induty_code: str | None) -> str:
    """induty_code(KSIC) → 섹터 라벨. 5→3→2자리 순으로 폴백.

    매핑 없으면 "기타".
    """
    code = _norm(induty_code)
    if not code:
        return "기타"
    # 5자리 정확 매치
    if len(code) >= 5 and code[:5] in KSIC5_TO_SECTOR:
        return KSIC5_TO_SECTOR[code[:5]]
    # 3자리 prefix
    if len(code) >= 3 and code[:3] in KSIC3_TO_SECTOR:
        return KSIC3_TO_SECTOR[code[:3]]
    # 2자리 prefix (sample 12개와 호환)
    if len(code) >= 2 and code[:2] in KSIC2_TO_SECTOR:
        return KSIC2_TO_SECTOR[code[:2]]
    return "기타"
