def map_induty(induty_code: str | None) -> str:
    """induty_code(5자리 KSIC) → 샘플 섹터 라벨.

    앞 2자리만 사용. 매핑 없으면 "기타".
    """
    if not induty_code:
        return "기타"
    code = str(induty_code).strip()
    if len(code) < 2:
        return "기타"
    return KSIC2_TO_SECTOR.get(code[:2], "기타")
